    private void jButtonBuscarCliente(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarCliente
        String nome = jTextFieldInformaCliente.getText();
        DefaultTableModel modeloTabela = (DefaultTableModel) jTableConsultaCliente.getModel();
        modeloTabela.setRowCount(0);
        ClienteController clienteController = new ClienteController();
        
        try {
            ArrayList<Cliente> clientes = clienteController.listarCliente(nome);
            clientes.forEach((Cliente cliente) -> {
            modeloTabela.addRow(new Object[]      {    
                                                  cliente.getCodCliente(),
                                                  cliente.getNome(),
                                                  cliente.getCPF(),
                                                  cliente.getEmail(),
                                                  cliente.getEndereco(),
                                                  cliente.getdtNascimento()
                                                  });
                    });
          jTableConsultaCliente.setModel(modeloTabela);
          
           
        } catch (ExceptionDAO e) {
        
            Logger.getLogger(TelaCadastroCliente.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_jButtonBuscarCliente

    private void jTableConsultaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableConsultaClienteMouseClicked
     if(evt.getClickCount()==2){
           Integer codFilme = (Integer) jTableConsultaCliente.getModel().getValueAt(jTableConsultaCliente.getSelectedRow(), 0);
           String nome = (String) jTableConsultaCliente.getModel().getValueAt(jTableConsultaCliente.getSelectedRow(), 1);
           String cpf = (String) jTableConsultaCliente.getModel().getValueAt(jTableConsultaCliente.getSelectedRow(), 2);
           String endereco = (String) jTableConsultaCliente.getModel().getValueAt(jTableConsultaCliente.getSelectedRow(), 3);
           String email = (String) jTableConsultaCliente.getModel().getValueAt(jTableConsultaCliente.getSelectedRow(), 4);
           Date dtNascimento = (Date) jTableConsultaCliente.getModel().getValueAt(jTableConsultaCliente.getSelectedRow(), 5);
          
     telaCadastroCliente.buscarCliente(codFilme, nome, cpf, endereco, email, dtNascimento);
     telaCadastroCliente.setVisible(true);
     this.dispose();
     }
    }//GEN-LAST:event_jTableConsultaClienteMouseClicked

    
    
    
